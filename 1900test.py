max_number = int(input("英単語テストの最大の出題範囲を入力してください: "))

# 新しい問題を設定
def set_new_question():
    global current_word
     # 指定された範囲内の単語をフィルタリング
    filtered_words = [word for word in Word if min_number <= word["number"] <= max_number]
    if not filtered_words:
        result_label.config(text="指定された範囲に単語がありません！", fg="red")
        return
    current_word = random.choice(filtered_words)
    firstword = current_word["word"][0]
    word_label.config(text=current_word["meanings"])
    answer_entry.delete(0, tk.END)
    answer_entry.insert(0,firstword)
    answer_entry.bind("<Return>", lambda event: check_answer())#エンターキーで答えを送信できるようにする



# 答えをチェック
def check_answer():
    global score, total_questions
    total_questions += 1
    user_answer = answer_entry.get().strip()
    if user_answer == current_word["word"]:
        score += 1
        result_label.config(text="正解！", fg="green")
    else:
        result_label.config(
            text=f"不正解！正しい答え: {current_word['word']}", fg="red"
        )
    score_label.config(text=f"スコア: {score}/{total_questions}")
    set_new_question()
    

